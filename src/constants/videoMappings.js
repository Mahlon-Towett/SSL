// Fallback sign for missing videos
export const FALLBACK_SIGN = 'Hello';

/**
 * Get video path for a sign with fallback
 */
export const getVideoPath = (signName) => {
  return VIDEO_MAPPINGS[signName] || VIDEO_MAPPINGS[FALLBACK_SIGN];
};

/**
 * Check if video exists for sign
 */
export const hasVideo = (signName) => {
  return VIDEO_MAPPINGS.hasOwnProperty(signName);
};

/**
 * Get all available signs
 */
export const getAllSigns = () => {
  return Object.keys(VIDEO_MAPPINGS);
};

/**
 * Get signs by category
 */
export const getSignsByCategory = (category) => {
  return SIGN_CATEGORIES[category] || [];
};

/**
 * Generate mappings from actual file list (utility function)
 * Use this to validate your mappings against actual files
 */
export const generateMappingsFromFiles = (fileList) => {
  const mappings = {};
  fileList.forEach(filename => {
    if (filename.endsWith('.mp4')) {
      const signName = filename.replace('.mp4', '');
      mappings[signName] = `./videos/${filename}`;
    }
  });
  return mappings;
};